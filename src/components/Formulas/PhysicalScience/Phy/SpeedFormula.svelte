	<!--Speed-->
	<Formula
		title="Speed"
		format="A"
		initialVariant="DEFAULT"
		values={{ a: m.SPEED, b: m.DISTANCE, c: m.TIME }}
		subscripts={{ a: 'NONE', b: 'NONE', c: 'NONE' }}
	/>
